"""
The Librarian — System 4: Unprompted Observation Engine

Generates contextual observations from session summaries and recent entries,
detecting cross-session patterns, stale threads, absence, contradictions, and progress.

Pure functions — no Librarian dependencies. Suitable for standalone integration
into host applications that manage sessions and entries.

Usage:
    from solitaire.core.observer import (
        generate_observations,
        check_suppression,
        format_observation,
        Observation,
        ObservationType,
    )

    observations = generate_observations(
        session_summaries=[{"session_id": "...", "summary": "...", "ended_at": "..."}],
        recent_entries=[{"id": "...", "content": "...", "category": "...", "tags": "...", "created_at": "..."}],
        current_message="..."
    )

    # Filter by suppression
    for obs in observations:
        if not check_suppression(obs.topic, suppressions):
            print(format_observation(obs))
"""

from dataclasses import dataclass, field
from enum import Enum
from typing import List, Optional, Dict, Any
from collections import Counter
from datetime import datetime, timedelta
import re


# ─── Observation Types ────────────────────────────────────────────────────────

class ObservationType(str, Enum):
    """Enumeration of possible observation types."""
    PATTERN = "cross_session_pattern"
    CONTRADICTION = "contradiction_alert"
    STALE_THREAD = "stale_thread"
    PROGRESS = "progress_tracking"
    ABSENCE = "absence_detection"


# ─── Core Data Structures ─────────────────────────────────────────────────────

@dataclass
class Observation:
    """
    A single observation generated by the observer engine.

    Represents a noteworthy pattern, anomaly, or contextual insight
    that should be surfaced to the Chief Librarian.
    """
    type: ObservationType
    summary: str                          # Human-readable observation
    supporting_entry_ids: List[str] = field(default_factory=list)
    topic: str = ""                       # What this is about
    suggested_framing: str = ""           # How the Chief should say it
    significance: float = 0.5             # 0.0-1.0 confidence/importance
    suppressible: bool = True             # Can user dismiss this


# ─── Suppression Tracking ─────────────────────────────────────────────────────

@dataclass
class Suppression:
    """Tracks a suppressed topic and when it expires."""
    topic: str
    suppressed_at: str
    expires_sessions: int
    sessions_remaining: int


